public class Switch {
     public static void main(String[] args) {
          int age;
          age = 3;

          switch (age) {
               case 2:
                    System.out.println("you can talk");
                    break;
               case 3:
                    System.out.println("you can walk");
                    break;
               case 5:
                    System.out.println("you can play game");
                    break;
               default:
                    System.out.println("i did't no how old are you");
                    break;
          }
     }
}
